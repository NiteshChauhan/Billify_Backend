const INVOICE_LANGUAGE_MODES = ["en", "ar", "hi", "en_ar", "en_hi"];

const normalizePdfLanguageMode = (value = "") => {
  const mode = String(value || "").toLowerCase();
  if (mode === "ar" || mode === "arabic") return "ar";
  if (mode === "hi" || mode === "hindi") return "hi";
  if (mode === "en_ar" || mode === "english_arabic") return "en_ar";
  if (mode === "en_hi" || mode === "english_hindi") return "en_hi";
  return "en";
};

const normalizePdfLanguage = (value = "") => {
  const mode = normalizePdfLanguageMode(value);
  if (mode === "ar") return "ar";
  if (mode === "hi") return "hi";
  return "en";
};

const getPdfLabels = (language = "en") =>
  LANGUAGE_LABELS[normalizePdfLanguage(language)] || LANGUAGE_LABELS.en;

const combineLabels = (left, right) => {
  const first = String(left || "").trim();
  const second = String(right || "").trim();
  if (!first) return second;
  if (!second) return first;
  return `${first} / ${second}`;
};

const getInvoiceLabels = (languageMode = "en") => {
  const mode = normalizePdfLanguageMode(languageMode);
  if (mode === "en_ar") {
    const merged = {};
    for (const key of Object.keys(LANGUAGE_LABELS.en)) {
      merged[key] = combineLabels(LANGUAGE_LABELS.en[key], LANGUAGE_LABELS.ar[key]);
    }
    return merged;
  }
  if (mode === "en_hi") {
    const merged = {};
    for (const key of Object.keys(LANGUAGE_LABELS.en)) {
      merged[key] = combineLabels(LANGUAGE_LABELS.en[key], LANGUAGE_LABELS.hi[key]);
    }
    return merged;
  }
  return getPdfLabels(mode);
};

module.exports = {
  INVOICE_LANGUAGE_MODES,
  normalizePdfLanguage,
  normalizePdfLanguageMode,
  getPdfLabels,
  getInvoiceLabels,
};
